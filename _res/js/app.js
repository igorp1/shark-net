function addNewNodeAt(latLng){

    var nodeType = document.getElementById("node-type-selector").value;
    nodes.push( NodeFactory(nodeType, latLng) );

}



function NodeFactory(type, position){

    // object 
    var Node = {
        latLng : {},
        maps_marker : null,
    }

    // create node instance
    var node = Object.create(Node);

    // generate node id
    var randomID_length = 3;
    node.id = Math.round((Math.pow(36, randomID_length + 1) - Math.random() * Math.pow(36, randomID_length))).toString(36).slice(1);

    // set position
    node.latLng = position;

    // create marker
    var markerIcon = {
        url: node_config.icons[type],
        scaledSize: new google.maps.Size(20, 35),
        origin: new google.maps.Point(0,0),
        anchor: new google.maps.Point(10,35),
        labelOrigin: new google.maps.Point(45, 15)
    };

    node.maps_marker = new google.maps.Marker({
        position: position,
        map: map,
        label: {
            text: node.id,
            color: node_config.colors[type],
            fontSize: "18px",
            fontWeight: "700",
            fontFamily: "Anonymous Pro"
        },
        icon: markerIcon,
        animation : google.maps.Animation.DROP
    });

    return node;

}
